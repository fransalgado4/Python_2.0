#2. Calcular el perímetro y área de un rectángulo dada su base y su altura. Calcular también el perímetro y área de un triangulo.

alturaTriangulo = 10
baseTriangulo = 5

perimetroTriangulo = 3 * baseTriangulo
areaTriangulo = (baseTriangulo * alturaTriangulo) / 2

print("Perimetro del triangulo es: ", perimetroTriangulo)
print("Area del triangulo es: ", areaTriangulo)