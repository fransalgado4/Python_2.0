#4. Dados dos números, mostrar la suma, resta, división y multiplicación de ambos.

numero1 = int(input("Dime el primer numero: "))
numero2 = int(input("Dime el segundo numero: "))

print("La suma de los numeros es: ", numero1 + numero2, ", la resta es: ", numero1 - numero2, ", la multiplicacion es: ", numero1 * numero2, " y la division es: ", round(numero1 / numero2, 2))